text = "My name is Kieran"

#This method converts all characters into uppercase
print(text.upper())

#This Method converts all characters into lowercase
print(text.lower())

#This method converts the first character into an uppercase
print(text.capitalize())

#This method returns the number of elements with the specified value
print(text.count("Kieran"))

#This method finds the occurance of the specified value
print(text.find("name"))

#This method replaces a specified phrase with a different phrase
print(text.replace("Kieran" ,"Kieran Atherton"))

#This method removes blank spaces
print(text.strip("Kieran"))







